/**
 * connect-oauth-callback — Netlify function (PUBLIC endpoint)
 *
 * Stripe redirects the trader's browser here after they authorise the Connect
 * OAuth flow. This function:
 *   1. Validates the state param (CSRF check) to recover the user ID
 *   2. Exchanges the `code` for a stripe_user_id via stripe.oauth.token()
 *   3. Writes stripe_user_id + connect status to the profiles row
 *   4. 302 redirects back to the app's card-payments screen
 *
 * GET — Stripe sends the user here via browser redirect (not a fetch)
 *
 * Query params supplied by Stripe:
 *   code  — one-time authorization code to exchange for access_token
 *   state — the state token we generated in connect-oauth-start.js
 *
 * Required env vars (set in Netlify dashboard):
 *   STRIPE_SECRET_KEY          — Stripe secret key
 *   STRIPE_CONNECT_CLIENT_ID   — used as the HMAC signing key for state validation
 *   VITE_SUPABASE_URL          — already set for the browser build
 *   SUPABASE_SERVICE_ROLE_KEY  — service-role key (bypasses RLS, server only)
 *   APP_URL                    — base URL to redirect back to after OAuth completes
 *
 * Response shapes:
 *   302  Location: <APP_URL>/#/settings?connected=1  — success
 *   302  Location: <APP_URL>/#/settings?connect_error=<reason>  — failure
 */

import Stripe from 'stripe';
import { createClient } from '@supabase/supabase-js';
import { createHmac } from 'crypto';

function redirect(location) {
  return {
    statusCode: 302,
    headers: { Location: location },
    body: '',
  };
}

/**
 * Validates the state token generated by connect-oauth-start.js.
 * Returns { valid: true, userId } on success, { valid: false } on failure.
 *
 * Token format: base64url(userId + '|' + nonce + '|' + expiry + '|' + sig)
 */
function validateStateToken(stateB64, secret) {
  try {
    const raw = Buffer.from(stateB64, 'base64url').toString('utf8');
    const parts = raw.split('|');
    if (parts.length !== 4) return { valid: false };
    const [userId, nonce, expiryStr, sig] = parts;
    const expiry = parseInt(expiryStr, 10);
    if (isNaN(expiry) || Date.now() > expiry) return { valid: false };
    const payload  = `${userId}|${nonce}|${expiryStr}`;
    const expected = createHmac('sha256', secret).update(payload).digest('hex');
    // Constant-time comparison to prevent timing attacks
    if (sig.length !== expected.length) return { valid: false };
    let diff = 0;
    for (let i = 0; i < sig.length; i++) diff |= sig.charCodeAt(i) ^ expected.charCodeAt(i);
    if (diff !== 0) return { valid: false };
    return { valid: true, userId };
  } catch {
    return { valid: false };
  }
}

export const handler = async function (event) {
  // ── 1. Validate env vars ─────────────────────────────────────────────────────
  const stripeSecretKey = process.env.STRIPE_SECRET_KEY;
  const connectClientId = process.env.STRIPE_CONNECT_CLIENT_ID;
  const supabaseUrl     = process.env.VITE_SUPABASE_URL;
  const serviceRoleKey  = process.env.SUPABASE_SERVICE_ROLE_KEY;
  const appBase         = (process.env.APP_URL || '').replace(/\/$/, '');

  const errorBase   = appBase ? `${appBase}/#/settings` : '/#/settings';
  const successBase = appBase ? `${appBase}/#/settings` : '/#/settings';

  if (!stripeSecretKey || !connectClientId || !supabaseUrl || !serviceRoleKey) {
    console.error('connect-oauth-callback: missing env vars');
    return redirect(`${errorBase}?connect_error=config`);
  }

  if (event.httpMethod !== 'GET') {
    return redirect(`${errorBase}?connect_error=bad_method`);
  }

  // ── 2. Extract query params ──────────────────────────────────────────────────
  const { code, state, error: stripeError } = event.queryStringParameters || {};

  // Stripe sends ?error=access_denied if the trader cancels
  if (stripeError) {
    console.warn('connect-oauth-callback: Stripe error returned', stripeError);
    return redirect(`${errorBase}?connect_error=${encodeURIComponent(stripeError)}`);
  }

  if (!code || !state) {
    return redirect(`${errorBase}?connect_error=missing_params`);
  }

  // ── 3. Validate state (CSRF check) ──────────────────────────────────────────
  const { valid, userId } = validateStateToken(state, connectClientId);
  if (!valid || !userId) {
    console.error('connect-oauth-callback: invalid state token');
    return redirect(`${errorBase}?connect_error=invalid_state`);
  }

  // ── 4. Exchange code for stripe_user_id ──────────────────────────────────────
  const stripe = new Stripe(stripeSecretKey, { apiVersion: '2024-06-20' });

  let stripeUserId;
  try {
    const response = await stripe.oauth.token({
      grant_type: 'authorization_code',
      code,
    });
    stripeUserId = response.stripe_user_id;
    if (!stripeUserId) throw new Error('stripe_user_id missing from OAuth response');
  } catch (err) {
    console.error('connect-oauth-callback: OAuth token exchange failed', err?.message);
    return redirect(`${errorBase}?connect_error=exchange_failed`);
  }

  // ── 5. Write to the profiles row ─────────────────────────────────────────────
  const adminClient = createClient(supabaseUrl, serviceRoleKey, {
    auth: { persistSession: false },
  });

  try {
    const { error: updateError } = await adminClient
      .from('profiles')
      .update({
        stripe_user_id: stripeUserId,
        stripe_connect_status: 'connected',
        stripe_connect_connected_at: new Date().toISOString(),
        stripe_connect_disconnected_at: null,
      })
      .eq('id', userId);

    if (updateError) {
      console.error('connect-oauth-callback: DB update failed', userId, updateError?.message);
      return redirect(`${errorBase}?connect_error=db_write_failed`);
    }
  } catch (err) {
    console.error('connect-oauth-callback: DB update threw', err?.message);
    return redirect(`${errorBase}?connect_error=db_write_failed`);
  }

  // ── 6. Redirect back to the app ──────────────────────────────────────────────
  return redirect(`${successBase}?connected=1`);
};
